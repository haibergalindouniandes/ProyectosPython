''' 
► Enunciado:
Dado un diccionario imprimir todas las claves en pantalla utilizando un bucle for.

► Entrada:
capitals = {
    'France': 'Paris',
    'Canada': 'Ottawa',
    'USA': 'Washington'
}

► Salida:
France
Canada
USA
'''

capitals = {
    'France': 'Paris',
    'Canada': 'Ottawa',
    'USA': 'Washington'
}

for key in capitals:
    print(key)
 