''' 
► Enunciado:
Dado un diccionario imprimir todas las claves en pantalla utilizando un bucle for.

► Entrada:

colors = {
    'color1': 'yellow', 
    'color2': 'blue',
    'color3': 'red',
}


► Salida:
yellow
blue
red
'''

colors = {
    'color1': 'yellow', 
    'color2': 'blue',
    'color3': 'red',
}

for value in colors.values():
    print(value)
    
 
 